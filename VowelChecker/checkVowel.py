def checkVowel(charList):
    for i in charList:
        if i == 'A':
            print(i, "Vowel")
        elif i == 'E':
            print(i, "Vowel")
        elif i == 'I':
            print(i, "Vowel")
        elif i == 'O':
            print(i, "Vowel")
        elif i == 'U':
            print(i, "Vowel")
        else:
            print(i, "Consonant")
                   
